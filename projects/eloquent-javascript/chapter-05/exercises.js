function flatten(arr) {
  let op = [];
  for (let i = 0; i < arr.length; i++){
    for (let ii = 0; ii < arr[i].length; ii++){
      op.push(arr[i][ii]);
    }
  }
  return op;

}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(val, test, update, body) {
  if (test(val)){
    body(val);
    return loop(update(val), test, update, body);
  }

}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(arr, test) {
  for (let i = 0; i < arr.length; i++){
    if (!test(arr[i])){
      return false;
    }
  }
  return true;
}

